using Microsoft.Extensions.Configuration;
using System.Runtime;
using AutoDo.RFPStorageComponent;
using AutoDo.StorageComponent;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.Configure<StoragePathSettings>(builder.Configuration.GetSection("ConnectionString"));
builder.Services.AddSingleton<IStorageComponent, StorageComponent>();


var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
